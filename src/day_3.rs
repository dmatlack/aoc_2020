#[derive(Debug, Copy, Clone)]
enum Coordinate {
    Tree,
    Empty,
}

impl Coordinate {
    fn from(c: char) -> Coordinate {
        match c {
            '.' => Coordinate::Empty,
            '#' => Coordinate::Tree,
            _ => panic!("Invalid character: {}", c),
        }
    }
}

struct Map {
    coordinates: Vec<Vec<Coordinate>>,
}

impl Map {
    fn from(s: &str) -> Map { 
        let mut coordinates: Vec<Vec<Coordinate>> = vec![];
        for line in s.split("\n") {
            let mut row: Vec<Coordinate> = vec![];
            for c in line.chars() {
                row.push(Coordinate::from(c));
            }
            coordinates.push(row);
        }
        Map { coordinates }
    }

    fn at(&self, x: usize, y: usize) -> Option<Coordinate> {
        if y >= self.coordinates.len() {
            None
        } else {
            let row = self.coordinates.get(y).unwrap();
            Some(row[x % row.len()])
        }
        
    }
}

struct Slope {
    right: usize,
    down: usize,
}

fn count_trees(slope: Slope) -> usize {
    let map = Map::from(INPUT);
    let mut position = (0, 0);
    let mut tree_count = 0;
    while let Some(coordinate) = map.at(position.0, position.1) {
        tree_count += match coordinate {
            Coordinate::Empty => 0,
            Coordinate::Tree => 1,
        };
        position.0 += slope.right;
        position.1 += slope.down;
    }
    tree_count
}

fn one() -> usize {
    count_trees(Slope { right: 3, down: 1 })
}

fn two() -> usize {
    count_trees(Slope { right: 1, down: 1 }) *
        count_trees(Slope { right: 3, down: 1 }) *
        count_trees(Slope { right: 5, down: 1 }) *
        count_trees(Slope { right: 7, down: 1 }) *
        count_trees(Slope { right: 1, down: 2 })
}

pub fn run() {
    println!("Day 3 Part 1: {:?}", one());
    println!("Day 3 Part 2: {:?}", two());
}
